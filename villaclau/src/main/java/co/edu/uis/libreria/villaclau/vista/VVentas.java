    private void cargar_modelo_Venta() {
        modelo_Venta.addColumn("Id_Venta");
        modelo_Venta.addColumn("fecha");
        modelo_Venta.addColumn("Cliente");
        modelo_Venta.addColumn("Cultivo");
        modelo_Venta.addColumn("precio");

        tbl_Ventas.setModel(modelo_Venta);
    }


