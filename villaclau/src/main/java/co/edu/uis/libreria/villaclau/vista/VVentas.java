    private void btn_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RegistrarActionPerformed
        // TODO add your handling code here:
        int id_venta = Integer.parseInt(txt_Id_Venta.getText());
        String fecha = txt_fecha.getText();
        int cliente = Integer.parseInt(txt_cliente.getText());
        int cultivo = Integer.parseInt(txt_cultivo.getText());
        double precio = Double.parseDouble(txt_precio.getText());

        Venta ve = new Venta(id_venta, fecha, cliente, cultivo, precio);
        Controlador_ventas.guardar_venta(ve);
        Controlador_ventas.cargar_ventas(tbl_Ventas, modelo_Venta);

        limpiar_espacios();

    }//GEN-LAST:event_btn_RegistrarActionPerformed

    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(JOptionPane.showInputDialog(null, "Escriba el ID venta"));
        Controlador_ventas.eliminar_venta(id, tbl_Ventas, modelo_Venta);
        Controlador_ventas.cargar_ventas(tbl_Ventas, modelo_Venta);
    }//GEN-LAST:event_btn_EliminarActionPerformed

    private void btn_ListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ListarActionPerformed
        // TODO add your handling code here:
        Controlador_ventas.cargar_ventas(tbl_Ventas, modelo_Venta);
    }//GEN-LAST:event_btn_ListarActionPerformed

    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        // TODO add your handling code here:
        int id_venta = Integer.parseInt(txt_Id_Venta.getText());
        String fecha = txt_fecha.getText();
        int cliente = Integer.parseInt(txt_cliente.getText());
        int cultivo = Integer.parseInt(txt_cultivo.getText());
        double precio = Double.parseDouble(txt_precio.getText());

        Venta ve = new Venta(id_venta, fecha, cliente, cultivo, precio);
        Controlador_ventas.actualizar_venta(ve);
        Controlador_ventas.cargar_ventas(tbl_Ventas, modelo_Venta);

        limpiar_espacios();
        txt_Id_Venta.setEnabled(true);

    }//GEN-LAST:event_btn_ActualizarActionPerformed

    private void tbl_VentasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_VentasMouseClicked
        // TODO add your handling code here:
        cargar_editar_venta();
        txt_Id_Venta.setEnabled(false);
    }//GEN-LAST:event_tbl_VentasMouseClicked

