    private void btn_filtrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_filtrarActionPerformed
        // TODO add your handling code here:
        double valor_1 = Double.parseDouble(JOptionPane.showInputDialog(null, "Primer rango de precio"));
        double valor_2 = Double.parseDouble(JOptionPane.showInputDialog(null, "Segundo rango de precio"));
        Controlador_ventas.cargar_ventas_precio(tbl_Ventas, modelo_Venta, valor_1, valor_2);
    }//GEN-LAST:event_btn_filtrarActionPerformed
    private void limpiar_espacios() {
        txt_Id_Venta.setText("");
        txt_fecha.setText("");
        txt_cliente.setText("");
        txt_cultivo.setText("");
        txt_precio.setText("");
    }

    private void cargar_editar_venta() {
        int fila_select = tbl_Ventas.getSelectedRow();
        txt_Id_Venta.setText((tbl_Ventas.getValueAt(fila_select, 0)).toString());
        txt_fecha.setText((tbl_Ventas.getValueAt(fila_select, 1)).toString());
        txt_cliente.setText((tbl_Ventas.getValueAt(fila_select, 2)).toString());
        txt_cultivo.setText((tbl_Ventas.getValueAt(fila_select, 3)).toString());
        txt_precio.setText((tbl_Ventas.getValueAt(fila_select, 4)).toString());
    }

